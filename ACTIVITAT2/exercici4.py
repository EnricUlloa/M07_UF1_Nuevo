#El usuario pone un primer valor
valor1 = float(input("Introdueix un primer valor: "))

#El usuario poner un segundo valor
valor2 = float(input("Introdueix un segon valor: "))

#Hacemos las operaciones
suma = valor1 + valor2
resta = valor1 - valor2
divisió = valor1 / valor2
multiplicació = valor1 * valor2

#Mostramos los resultados
print(f"La suma es: {suma}")
print(f"La resta es: {resta}")
print(f"La divisió es: {divisió}")
print(f"La multiplicació es: {multiplicació}")